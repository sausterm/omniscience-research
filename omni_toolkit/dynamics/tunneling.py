"""
Tunneling and Instanton Methods
================================

Core capability: compute tunneling amplitudes exp(-S_eff) on
non-compact symmetric spaces.

Key quantities:
  - Instanton action S_inst on G/H
  - Effective degrees of freedom N_eff from geometry
  - Tunneling amplitude A = exp(-S_inst)
  - Non-perturbative scale Lambda_NP = Lambda_UV * exp(-S_inst)

This is the central module that transfers to condensed matter,
catalysis, LENR, and topological QC applications.

Generalized from the instanton estimate in coleman_weinberg_su2R.py.
"""

import numpy as np
import math
from dataclasses import dataclass, field
from typing import Optional, Tuple, Callable, List, Dict, Any
from scipy.integrate import solve_ivp, quad
from scipy.optimize import brentq, minimize_scalar, minimize


_trapz = getattr(np, 'trapezoid', getattr(np, 'trapz', None))


@dataclass
class InstantonConfig:
    """Configuration for an instanton computation on G/H."""

    coupling: float          # Gauge coupling g at the relevant scale
    Lambda_UV: float         # UV scale (e.g., M_C)
    N_eff: Optional[float] = None  # Effective DOF (computed or specified)
    topological_charge: int = 1     # Winding number


class InstantonAction:
    """Compute instanton actions on symmetric spaces."""

    def __init__(self, dim_fibre: int, dim_isotropy: int):
        """
        Args:
            dim_fibre: Dimension of the tangent space p
            dim_isotropy: Dimension of the isotropy algebra h
        """
        self.dim_p = dim_fibre
        self.dim_h = dim_isotropy

    def standard_action(self, g: float, n: int = 1) -> float:
        """Standard instanton action S = 8 pi^2 n / g^2.

        This is the BPST result for SU(2) instantons. For other groups,
        the action is S = 8 pi^2 n / (N_eff * g^2) where N_eff accounts
        for the effective number of participating degrees of freedom.
        """
        return 8 * math.pi**2 * n / g**2

    def effective_action(self, g: float, N_eff: float, n: int = 1) -> float:
        """Effective instanton action with N_eff correction.

        S_eff = 8 pi^2 n / (N_eff * g^2)

        N_eff > 1 means the instanton spreads over multiple DOF,
        reducing the action per DOF and enhancing tunneling.
        """
        return 8 * math.pi**2 * n / (N_eff * g**2)

    def tunneling_amplitude(self, S: float) -> float:
        """Tunneling amplitude A = exp(-S)."""
        return math.exp(-S)

    def nonperturbative_scale(self, Lambda_UV: float, g: float,
                               N_eff: float, n: int = 1) -> float:
        """Non-perturbative scale Lambda_NP = Lambda_UV * exp(-S_eff).

        This is the scale generated by dimensional transmutation
        via instantons.
        """
        S = self.effective_action(g, N_eff, n)
        return Lambda_UV * math.exp(-S)

    def N_eff_for_target_scale(self, Lambda_UV: float, Lambda_target: float,
                                g: float) -> float:
        """Compute N_eff needed to generate a specific scale.

        Given Lambda_target = Lambda_UV * exp(-8pi^2 / (N_eff * g^2)),
        solve for N_eff.
        """
        S_needed = math.log(Lambda_UV / Lambda_target)
        return 8 * math.pi**2 / (S_needed * g**2)

    def scan_N_eff(self, Lambda_UV: float, g: float,
                    N_eff_range: Tuple[float, float] = (1, 30)) -> list:
        """Scan instanton scale as a function of N_eff.

        Returns list of (N_eff, Lambda_NP, log10_Lambda_NP).
        """
        results = []
        for N in np.linspace(N_eff_range[0], N_eff_range[1], 100):
            Lambda_NP = self.nonperturbative_scale(Lambda_UV, g, N)
            log_Lambda = math.log10(max(Lambda_NP, 1e-300))
            results.append((N, Lambda_NP, log_Lambda))
        return results


class N_eff_Calculator:
    """Compute N_eff from the geometry of the symmetric space.

    N_eff is the effective number of degrees of freedom participating
    in the instanton. It depends on:
    1. The dimension of the fibre (dim_p)
    2. The topology (pi_3 of the isotropy group)
    3. The metric structure (eigenvalues of the DeWitt metric)
    """

    def __init__(self, dim_p: int, dim_h: int):
        self.dim_p = dim_p
        self.dim_h = dim_h

    def from_dimension_counting(self) -> float:
        """Simplest estimate: N_eff = dim_p.

        This counts all directions in the tangent space as participating.
        Gives an upper bound.
        """
        return float(self.dim_p)

    def from_metric_eigenvalues(self, eigenvalues: np.ndarray) -> float:
        """Compute N_eff from the metric eigenvalue spectrum.

        N_eff = (sum |lambda_i|)^2 / (sum lambda_i^2)

        This is the "participation ratio" — it measures how many
        eigenvalues contribute significantly. For equal eigenvalues,
        N_eff = dim_p. For one dominant eigenvalue, N_eff = 1.
        """
        abs_eigs = np.abs(eigenvalues[np.abs(eigenvalues) > 1e-15])
        if len(abs_eigs) == 0:
            return 0.0
        return float(np.sum(abs_eigs)**2 / np.sum(abs_eigs**2))

    def from_positive_sector(self, positive_eigenvalues: np.ndarray) -> float:
        """N_eff from V+ sector only (gauge DOF)."""
        return self.from_metric_eigenvalues(positive_eigenvalues)

    def from_instanton_profile(self, action_profile: Callable,
                                R_range: Tuple[float, float] = (0.01, 100)) -> float:
        """Compute N_eff from the radial profile of the instanton solution.

        N_eff = integral of the action density over the instanton profile.
        """
        S_total, _ = quad(action_profile, R_range[0], R_range[1])
        S_unit = 8 * math.pi**2  # Standard BPST action
        return S_unit / max(S_total, 1e-30)


class BarrierTunneling:
    """Compute tunneling rates through potential barriers.

    This is the general framework applicable to:
    - Coulomb barrier (LENR)
    - Phase transitions (condensed matter)
    - Catalytic reactions (transition states)
    """

    def __init__(self, potential: Callable, mass: float = 1.0):
        """
        Args:
            potential: V(x) — the potential barrier
            mass: Effective mass (or reduced mass)
        """
        self.V = potential
        self.mass = mass

    def wkb_action(self, x_a: float, x_b: float, E: float,
                    n_points: int = 1000) -> float:
        """WKB tunneling action S = integral sqrt(2m(V-E)) dx.

        This is the semiclassical approximation valid when S >> hbar.
        """
        x = np.linspace(x_a, x_b, n_points)
        integrand = np.zeros(n_points)
        for i, xi in enumerate(x):
            V_minus_E = self.V(xi) - E
            if V_minus_E > 0:
                integrand[i] = math.sqrt(2 * self.mass * V_minus_E)
        # np.trapezoid (numpy >= 2.0) or np.trapz (numpy < 2.0)
        _trapz = getattr(np, 'trapezoid', getattr(np, 'trapz', None))
        return float(_trapz(integrand, x))

    def wkb_rate(self, x_a: float, x_b: float, E: float) -> float:
        """WKB tunneling rate Gamma ~ exp(-2S/hbar).

        Returns the exponential suppression factor (no prefactor).
        """
        S = self.wkb_action(x_a, x_b, E)
        return math.exp(-2 * S)

    def bounce_solution(self, x_init: float, E: float,
                         x_range: Tuple[float, float] = (0, 100),
                         n_points: int = 1000) -> Tuple[np.ndarray, np.ndarray]:
        """Find the bounce (instanton) solution in the inverted potential.

        The bounce satisfies: x'' = -dV/dx with V -> -V (Euclidean time).
        """
        def dV_dx(x):
            eps = abs(x) * 1e-8 + 1e-12
            return (self.V(x + eps) - self.V(x - eps)) / (2 * eps)

        def equations(t, y):
            x, v = y
            return [v, dV_dx(x)]  # Note: +dV/dx because inverted potential

        sol = solve_ivp(
            equations, x_range, [x_init, 0.0],
            method='RK45', max_step=0.1,
            t_eval=np.linspace(x_range[0], x_range[1], n_points)
        )
        return sol.t, sol.y[0]

    def enhanced_tunneling(self, S_bare: float, N_eff: float) -> float:
        """Tunneling rate with N_eff collective enhancement.

        The idea: if N_eff modes participate collectively,
        the effective action is reduced: S_eff = S_bare / N_eff.
        """
        return math.exp(-S_bare / N_eff)


