# For any complex structure J on R^6, with J-adapted orthonormal basis
# {e_1, Je_1, e_2, Je_2, e_3, Je_3}, the holomorphic sectional curvature
# sum is:
# E(J) = Σ_{a=1}^3 R(e_a, Je_a, Je_a, e_a)

# In terms of the Riemann tensor components:
# E(J) = Σ_{a=1}^3 Σ_{i,j,k,l} (e_a)_i (Je_a)_j (Je_a)_k (e_a)_l R_{ijkl}

# This is a quartic form in J, which we can evaluate for specific J's.

# Strategy: parameterize J via SO(6) rotations of J_std
# But use SPECIFIC well-chosen rotations, not random search.

J_std = np.zeros((6, 6))
for k in range(3):
    J_std[2*k, 2*k+1] = -1
    J_std[2*k+1, 2*k] = 1

def compute_E(J):
    """Holomorphic sectional curvature sum for complex structure J.
    Uses the full Riemann tensor."""
    eigs_J, vecs_J = np.linalg.eig(J)
    plus_i = np.where(np.abs(eigs_J - 1j) < 0.1)[0]

    E = 0.0
    for a in range(3):
        z = vecs_J[:, plus_i[a]]
        v = np.real(z)
        nv = np.linalg.norm(v)
        if nv < 1e-10:
            v = np.imag(z)
            nv = np.linalg.norm(v)
        v = v / nv
        Jv = J @ v

        # R(v, Jv, Jv, v)
        E += sum(v[i]*Jv[j]*Jv[k]*v[l]*R[i,j,k,l]
                 for i in range(n) for j in range(n)
                 for k in range(n) for l in range(n))
    return E

E_std = compute_E(J_std)
print(f"\nE(J_std) = {E_std:.8f}")

# Now evaluate E for J rotated by specific SO(6) generators
# The 6 generators of m (complement of u(3) in so(6)) parameterize CP³

# Find m-generators for J_std
so6_gens = []
