import numpy as np
from scipy.linalg import expm
np.set_printoptions(precision=8, suppress=True, linewidth=120)

# =====================================================================
# SETUP
# =====================================================================

eta = np.diag([-1.0, 1.0, 1.0, 1.0])

def dewitt(h, k):
    t1 = np.einsum('mr,ns,mn,rs', eta, eta, h, k)
    trh = np.einsum('mn,mn', eta, h)
    trk = np.einsum('mn,mn', eta, k)
    return t1 - 0.5 * trh * trk

def proj_k(X):
    return (X - eta @ X.T @ eta) / 2

def proj_p(X):
    return (X + eta @ X.T @ eta) / 2

def bracket(X, Y):
    return X @ Y - Y @ X

# Build p-basis
p_raw = []
for i in range(4):
    for j in range(4):
        E = np.zeros((4, 4)); E[i,j] = 1.0
        Ep = proj_p(E)
        if np.linalg.norm(Ep) > 1e-10:
            p_raw.append(Ep)

def indep_basis(mats):
    n = mats[0].shape[0]
    vecs = np.array([M.flatten() for M in mats])
    U, S, Vt = np.linalg.svd(vecs, full_matrices=False)
    rank = np.sum(S > 1e-10)
    return [v.reshape(n, n) for v in Vt[:rank]]

p_ind = indep_basis(p_raw)

# DeWitt metric and diagonalization
Gp = np.zeros((10, 10))
for a in range(10):
    for b in range(10):
        Gp[a, b] = dewitt(p_ind[a], p_ind[b])

eigvals_p, eigvecs_p = np.linalg.eigh(Gp)
pos_p = np.where(eigvals_p > 1e-10)[0]
neg_p = np.where(eigvals_p < -1e-10)[0]

# V+ orthonormal basis as 4×4 matrices
vp = []
for idx in pos_p:
    v = eigvecs_p[:, idx]
    mat = sum(v[a] * p_ind[a] for a in range(10))
    mat = mat / np.sqrt(dewitt(mat, mat))
    vp.append(mat)

# V- orthonormal basis
vm = []
for idx in neg_p:
    v = eigvecs_p[:, idx]
    mat = sum(v[a] * p_ind[a] for a in range(10))
    mat = mat / np.sqrt(-dewitt(mat, mat))
    vm.append(mat)

print("=" * 72)
print("ROUTE B (FAST): ANALYTICAL CP³ POTENTIAL")
print("=" * 72)

# =====================================================================
# PART 1: FULL RIEMANN TENSOR ON V+
# =====================================================================

print("\n--- Computing Riemann tensor on V+ ---")

n = len(vp)  # = 6
R = np.zeros((n, n, n, n))
for i in range(n):
    for j in range(n):
        bij = bracket(vp[i], vp[j])
        bij_k = proj_k(bij)
        for k in range(n):
            Rk = -bracket(bij_k, vp[k])
            Rk_p = proj_p(Rk)
            for l in range(n):
                R[i,j,k,l] = dewitt(Rk_p, vp[l])

# Verify symmetries
print(f"R_{'{ijkl}'} symmetry checks:")
max_skew = max(abs(R[i,j,k,l] + R[j,i,k,l])
               for i in range(n) for j in range(n)
               for k in range(n) for l in range(n))
print(f"  R_ijkl = -R_jikl: max violation = {max_skew:.2e}")

max_pair = max(abs(R[i,j,k,l] - R[k,l,i,j])
               for i in range(n) for j in range(n)
               for k in range(n) for l in range(n))
print(f"  R_ijkl = R_klij: max violation = {max_pair:.2e}")

# Ricci tensor
Ric = np.zeros((n, n))
for i in range(n):
    for j in range(n):
        Ric[i,j] = sum(R[k,i,k,j] for k in range(n))

print(f"\nRicci tensor:")
print(Ric)

ric_evals, ric_evecs = np.linalg.eigh(Ric)
print(f"\nRicci eigenvalues: {np.sort(ric_evals)}")

# Scalar curvature
scal = np.trace(Ric)
print(f"Scalar curvature on V+: {scal:.6f}")

