m_gens = indep_basis(m_gens) if m_gens else []
print(f"\nCP³ tangent directions (m-generators): {len(m_gens)} (expected 6)")

# Evaluate E along each m-direction
print("\nE(J) along geodesics from J_std:")
print(f"{'t':>8} | {'E(J(t))':>12} | direction")
print("-" * 50)

for m_idx in range(len(m_gens)):
    for t in [0.0, 0.1, 0.3, 0.5, 0.7, 1.0, np.pi/4, np.pi/2]:
        Rot = expm(t * m_gens[m_idx])
        J_t = Rot @ J_std @ Rot.T
        E_t = compute_E(J_t)
        if t == 0:
            label = f"m_{m_idx}"
        else:
            label = ""
        print(f"{t:8.4f} | {E_t:12.6f} | {label}")
