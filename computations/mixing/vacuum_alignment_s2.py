def V_CW(v, epsilon, mu=1.0):
    """
    Coleman-Weinberg potential from fermion loop contribution.

    The fermion masses depend on v through the FN charges.
    V = -(N_c/16π²) Σ_f m_f⁴ [ln(m_f²/μ²) - 3/2]
    """
    v = np.array(v) / np.linalg.norm(v)
    N_c = 3  # colors

    # Compute Yukawa eigenvalues (proportional to masses)
    Y = yukawa_matrix(v, epsilon)
    singular_vals = np.linalg.svd(Y, compute_uv=False)

    # The potential (up to overall normalization)
    V = 0
    for m in singular_vals:
        if m > 1e-15:
            m4 = m**4
            V -= N_c * m4 * (np.log(m**2 / mu**2) - 1.5)

    # Add up-type contribution (with slightly different Yukawa)
    # For simplicity, assume up-type = down-type × tan(β) factor
    for m in singular_vals:
        if m > 1e-15:
            m_up = m * 1.7  # rough m_t/m_b factor
            m4 = m_up**4
            V -= N_c * m4 * (np.log(m_up**2 / mu**2) - 1.5)

    return V / (16 * np.pi**2)

