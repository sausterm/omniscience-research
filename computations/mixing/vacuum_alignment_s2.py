def mass_matrix_overlap(v, epsilon):
    """
    Build mass matrix from overlap integrals between
    generation wavefunctions and the vacuum direction.

    The (a,b) element of the Yukawa matrix is:
      Y_{ab} ~ ∫ ψ_a*(x) Φ(x) ψ_b(x) dx

    where ψ_a is the wavefunction of generation a, localized
    near the complex structure J_a, and Φ is the Higgs field
    localized near the vacuum direction v.

    In the Gaussian approximation:
      Y_{ab} ~ exp(-d²(J_a, v)/2σ² - d²(J_b, v)/2σ²)
    where d(J_a, v) is the geodesic distance on S² and
    σ² ~ 1/(2 ln(1/ε²)) is the localization width.
    """
    v = np.array(v, dtype=float)
    v = v / np.linalg.norm(v)

    # Geodesic distances on S²
    # The three generation directions correspond to the three
    # coordinate axes in Im(H) ≅ R³
    gen_dirs = np.eye(3)  # I→(1,0,0), J→(0,1,0), K→(0,0,1)

    # Geodesic distance on S²: d(u,v) = arccos(u·v)
    distances = []
    for g in gen_dirs:
        cos_d = np.clip(np.dot(g, v), -1, 1)
        distances.append(np.arccos(cos_d))

    distances = np.array(distances)

    # Localization width from ε
    # σ² = 1/(2 ln(1/ε²)) so that ε^(q²) = exp(-q²σ²/...)
    # Actually, for the FN mechanism: Y ~ ε^q where q is the charge
    # The charge is proportional to the angle, so:
    # Y_{ab} ~ ε^{f(d_a) + f(d_b)} where f(d) maps distance to charge

    # For small angles: f(d) ≈ d/(π/3) (normalized so that f(π/2) = 3/2)
    # For the three axes separated by π/2 on S²:
    # The heaviest generation has d = 0 (along v)
    # The others have d = π/2 (perpendicular to v)

    # Charge assignment: q_a = round(d_a / (π/6))
    # gives q = (0, 3, 3) for v = K, which is wrong.

    # Better: use the actual distance as a continuous charge
    # Y_{ab} = ε^{d_a/d_0 + d_b/d_0} where d_0 = π/6 (= 30°)

    d_0 = np.pi / 6  # natural scale from S² geometry

    Y = np.zeros((3, 3))
    for a in range(3):
        for b in range(3):
            Y[a, b] = epsilon ** (distances[a]/d_0 + distances[b]/d_0)

    return Y

