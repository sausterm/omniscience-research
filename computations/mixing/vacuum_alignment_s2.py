def yukawa_matrix(v, epsilon):
    """
    Build the Yukawa matrix Y_{ab} from FN charges.
    Y_{ab} ~ ε^{(q_a + q_b)/2} with O(1) coefficients.
    """
    q = fn_charges(v)
    Y = np.zeros((3, 3))
    for a in range(3):
        for b in range(3):
            # The exponent is the average charge
            exponent = (q[a] + q[b]) / 2
            Y[a, b] = epsilon ** exponent
    return Y

def ckm_from_vacuum(v, epsilon, delta_ud=0.0):
    """
    Compute CKM matrix from vacuum direction v.

    delta_ud: small up-down asymmetry parameter (from SU(2)_R connection)
    """
    v = np.array(v) / np.linalg.norm(v)

    # Up-type and down-type Yukawa matrices
    # They differ by the SU(2)_R asymmetry
    Y_d = yukawa_matrix(v, epsilon)
    Y_u = yukawa_matrix(v, epsilon * (1 + delta_ud))

    # Diagonalize: Y = U Σ V†
    # Mass matrix M = Y·v_H, so M†M = v²Y†Y
    # The CKM is V = U_u† · U_d

    u_u, s_u, vh_u = np.linalg.svd(Y_u)
    u_d, s_d, vh_d = np.linalg.svd(Y_d)

    V_ckm = u_u.T @ u_d

    return np.abs(V_ckm), s_u / s_u[0], s_d / s_d[0]

